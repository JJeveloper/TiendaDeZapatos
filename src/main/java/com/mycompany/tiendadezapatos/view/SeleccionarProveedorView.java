    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed

        if (seleccionarventana.equals("agregarmercancia")) {

            int id = (int) tblProveedor.getModel().getValueAt(tblProveedor.getSelectedRow(), 0);
            try {

                this.setVisible(false);

                AgregarMercaderiaView agregarMercaderiaView = new AgregarMercaderiaView();

                //Enviar id del proveedor
                agregarMercaderiaView.ObtenerIdProveedor(id);

                agregarMercaderiaView.setVisible(true);
                PrincipalView.panelPrincipal.add(agregarMercaderiaView);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "No se puede obtener el proveedor " + e);

            }

        } else if (seleccionarventana.equals("hacerpedido")) {

            try {

                int id = (int) tblProveedor.getModel().getValueAt(tblProveedor.getSelectedRow(), 0);
                
               // this.setVisible(false);

               this.setClosed(true);
               
               RealizarPedidoView seleccionarMercaderiaView = new RealizarPedidoView();

                //Enviar id del proveedor
                seleccionarMercaderiaView.obtenerIdProveedor(id);

                seleccionarMercaderiaView.setVisible(true);
                PrincipalView.panelPrincipal.add(seleccionarMercaderiaView);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "No se puede obtener el proveedor " + e);

            }

        }


    }//GEN-LAST:event_btnSeleccionarActionPerformed

