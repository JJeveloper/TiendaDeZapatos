    private void CargarTabla() {

        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");

        List<Proveedor> lista = proveedorDAO.obtenerProveedor();

        if (lista.isEmpty()) {

            JOptionPane.showMessageDialog(this, "No existen proveedores");

        } else {

            for (Proveedor p : lista) {
                dtmProveedor.addRow(new Object[]{
                    p.getIdproveedor(), p.getRuc(), p.getCodigoproveedor(), p.getNombre(), p.getTelefono(),
                    p.getCorreo(), formato.format(p.getFechaderegistro())
                });
            }

        }

    }

